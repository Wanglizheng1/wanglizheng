package day8;
//演示4种进制
public class BinaryTest {
    public static void main(String[] args) {
        int n1 = 0b1010; //2进制
        int n2 = 1010;//10进制
        int n3 = 01010;//8进制
        int n4 = 0x10101;//16进制
        System.out.println("n1="+n1);
        System.out.println("n2="+n2);
        System.out.println("n3="+n3);
        System.out.println("n4="+n4);
        System.out.println(0x23A);
    }
}
