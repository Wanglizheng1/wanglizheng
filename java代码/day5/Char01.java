package day5;
//演示char的基本使用
public class Char01 {
    public static void main(String[] args) {
       char c1 = 'a';
       char c2 = '\t';
       char c3  ='王';
       char c4  = 97;  //字符类型可直接存放数字
        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);
        System.out.println(c4);//输出97表示的字符

    }
}
